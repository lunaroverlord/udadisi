public class Tweet {
	public Tweet(String user, String content, String location, String place,
			String date, String url) {
		super();
		this.user = user;
		this.content = content;
		this.location = location;
		this.place = place;
		this.date = date;
		this.url = url;
	}

	public String user;
	public String content;
	public String location;
	public String place;
	public String date;
	public String url;
}
